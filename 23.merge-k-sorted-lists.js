/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode[]} lists
 * @return {ListNode}
 */
var mergeKLists = function(lists) {
  return lists.reduce((res, list) => merge(res, list), null);
};

function merge(l1, l2) {
  const dummy = new ListNode();
  let cur = dummy;

  for (; l1 && l2; cur = cur.next) {
    if (l1.val < l2.val) {
      cur.next = l1;
      l1 = l1.next;
    } else {
      cur.next = l2;
      l2 = l2.next;
    }
  }

  l1 && (cur.next = l1);
  l2 && (cur.next = l2);

  return dummy.next;
}