/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */

/**
 * @param {ListNode} headA
 * @param {ListNode} headB
 * @return {ListNode}
 */
var getIntersectionNode = function(headA, headB) {
  let curA = headA;
  let curB = headB;

  while (curA && curB) {
    curA = curA.next;
    curB = curB.next;
  }

  if (!curA) {
    for (; curB; curB = curB.next) {
      headB = headB.next;
    }
  }

  if (!curB) {
    for (; curA; curA = curA.next) {
      headA = headA.next;
    }
  }

  while (headA !== headB) {
    headA = headA.next;
    headB = headB.next;
  }

  return headA;
};
